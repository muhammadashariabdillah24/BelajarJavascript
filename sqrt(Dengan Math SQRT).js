// Fungsi SQRT berfungsi untuk mencari akar dari suatu angka

function panggilSQRT() {
    
    console.log(Math.sqrt(15));
    console.log(Math.sqrt(25));
    console.log(Math.sqrt(50));
    console.log(Math.sqrt(75));
    console.log(Math.sqrt(100));
}

panggilSQRT()

