// Fungsi Min Digunakan untuk mencari nilai paling terkecil dari beberapa nilai
// Fungsi Max Digunakan 

function panggilMinMax() {

    let min = Math.min(5, 1000, 10, 50, 1)

    console.log('Nilai Terkecil = ',min);

    let max = Math.max(5, 1000, 10, 50, 1)

    console.log('Nilai Terbesar = ',max);
}

panggilMinMax()

// Hasil

// Nilai Terkecil =  1
// Nilai Terbesar =  1000