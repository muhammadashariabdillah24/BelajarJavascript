// Fungsi CharAt adalah mencari sebuah karakter dengan index dari karakter tersebut

function panggilCharAt() {
    
    let kata = "Aku Bootcamp Di Niomic"

    console.log(kata.charAt(0));
    // u
    console.log(kata.charAt(5));
    // *spasi

    console.log(kata.charAt(21));

}

panggilCharAt()

// Hasil

// A
// o
// c