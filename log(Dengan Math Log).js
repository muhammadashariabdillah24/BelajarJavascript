// Fungsi log merupakan logaritma murni

function panggilLogaritma() {
    
    console.log(Math.log(1));
    console.log(Math.log(10));

    
    console.log(Math.log2(356));
    // Arti Log2 adalah ( 2 log 356)
    console.log(Math.log10(100));
    // Arti Log10 adalah ( 10 Log 100 )
}

panggilLogaritma()