// Math merupakan object javascript yang berisi berbagai property dan method
// untuk pemrosesan angka

console.log(Math.E); 
// Berfungsi sebagai angka logaritma natural

console.log(Math.LN10);
// Berfungsi sebagai angka logaritma natural 10

console.log(Math.LN2);
// Berfungsi sebagai angka logaritma natural 2

console.log(Math.LOG10E);
// Berfungsi sebagai angka logaritma natural e dengan basis 10

console.log(Math.LOG2E);
// Berfungsi sebagai angka logaritma natural e dengan basis 2

console.log(Math.PI);
// Berfungsi sebagai angka pi 3.14

console.log(Math.SQRT1_2);
// Berfungsi angka 1 dibagi dengan kuadrat 2

console.log(Math.SQRT2);
// Berfungsi angka akar kuadrat 2