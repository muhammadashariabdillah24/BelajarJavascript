function panggilLowerUpper() {

    let kata = "Aku Belajar Di Niomic"

    console.log('Ubah Kalimat Menjadi Kecil Dengan toLowerCase = ',kata.toLowerCase());

    console.log('Ubah kalimat Menjadi Besar Dengan toUpperCase = ',kata.toUpperCase());
}

panggilLowerUpper()




