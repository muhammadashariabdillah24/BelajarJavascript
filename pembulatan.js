// Fungsi Yang Dipakai Untuk Pembulatan
// Math Ceil, Math Floor, Math Round

function panggilMathCeil() {
    
    var x = 12.3

    console.log(
        'Fungsi Math Ceil Yang Akan Membulatkan Nilainya Keatas | Hasil ',
        Math.ceil(x)
    );

    console.log('\n');

    console.log(
        'Fungsi Math Floor Yang Akan Membulatkan Nilainya Kebawah | Hasil ',
        Math.floor(x)
    );

    console.log('\n');

    console.log(
        'Fungsi Math Round Yang Akan Membulatkan Ke Angka Yang Terdekat | Hasil ',
        Math.round(x)
    );
}

panggilMathCeil()

// Hasil

// Fungsi Math Ceil Yang Akan Membulatkan Nilainya Keatas 
// | Hasil  13


// Fungsi Math Floor Yang Akan Membulatkan Nilainya Kebawah 
// | Hasil  12


// Fungsi Math Round Yang Akan Membulatkan Ke Angka Yang Terdekat 
// | Hasil  12
